package org.example.sciencecalc.physics;

public enum SpecificHeatCapacityUnit {
    JOULES_PER_KILOGRAM_PER_KELVIN, // J/(kg·K)
    JOULES_PER_GRAM_PER_KELVIN, // J/(g·K)
    CALORIES_PER_KILOGRAM_PER_KELVIN, // cal/(kg·K)
    CALORIES_PER_GRAM_PER_KELVIN, // cal/(g·K)
    KILOCALORIES_PER_KILOGRAM_PER_KELVIN, // kcal/(kg·K)
    JOULES_PER_KILOGRAM_PER_CELSIUS, // J/(kg·°C)
    JOULES_PER_GRAM_PER_CELSIUS, // J/(g·°C)
    CALORIES_PER_KILOGRAM_PER_CELSIUS, // cal/(kg·°C)
    CALORIES_PER_GRAM_PER_CELSIUS, // cal/(g·°C)
    KILOCALORIES_PER_KILOGRAM_PER_CELSIUS // kcal/(kg·°C)
}
